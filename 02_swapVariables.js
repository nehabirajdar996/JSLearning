console.log("******* step1: swap two variables value ******");
var sweety="Sweety";
var cutie="Cutie";
console.log("* Before swapping *");
console.log("sweety:",sweety);
console.log("cutie:",cutie);

console.log("* After swapping *");
var temp;
temp=sweety;
sweety=cutie;
cutie=temp;
console.log("sweety:",sweety);
console.log("cutie:",cutie);

